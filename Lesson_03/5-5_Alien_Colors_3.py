# Version 1: Alien color is green
alien_color: str = "green"

# Check the alien's color and assign points depending on the color
if alien_color == "green":
    print("Player just earned 5 points for shooting the alien.")
elif alien_color == "yellow":
    print("Player just earned 10 points.")
else:
    print("Player just earned 15 points.")

