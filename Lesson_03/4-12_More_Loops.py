# Exercise 4-12: More Loops

# List of fruits
fruits: list[str] = ["apple", "banana", "cherry", "orange", "grape"]

# List of cheeses
cheeses: list[str] = ["cheddar", "brie", "gorgonzola", "parmesan", "mozzarella"]

# Print the title for the fruits list
print("Fruits:")
# Iterate through the fruits list and print each fruit
for fruit in fruits:
    print(fruit)
# Print a blank line for spacing between the two lists
print()

# Print the title for the cheeses list
print("Cheeses:")
# Iterate through the cheeses list and print each cheese
for cheese in cheeses:
    print(cheese)
