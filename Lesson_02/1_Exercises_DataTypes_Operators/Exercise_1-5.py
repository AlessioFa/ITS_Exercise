# Initialize a Fahrenhein temperature to a variable
fahrenheit_temp: int = 88

# Convert Fahrenheit to Celsius using the formula
celsius_temp: float = 5*(fahrenheit_temp - 32)/9

# Display the Fahrenheit temperature
print(f"Temperature in Fahrenheit is: {fahrenheit_temp}")

# Display the Celsius temperature rounded to one decimal
print(f"Equivalent temperature in Celsius: {celsius_temp:.1f}")
